class Arrays 
{
	public static void main(String[] args) 
	{
		int[] numbers;
		numbers = new int [5];
		numbers[0] = 35;
		numbers[1] = 45;
		numbers[2] = 55;
		numbers[3] = 65;
		numbers[4] = 75;

		System.out.print(numbers[1]);

		String[] candy;
		candy = new String [2];
		candy[0] = "mint";
		candy[1] = "rock";

		System.out.print(candy[0]);

		int[] num = {5, 9, 45};
		System.out.print(num[1]);
	}	
}
